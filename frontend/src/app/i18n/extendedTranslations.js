const ORDER_STATUS_KEYS = {
  Pending: "pending",
  Accepted: "status_accepted",
  "Out for Delivery": "status_out_for_delivery",
  Delivered: "delivered",
  "Return Requested": "status_return_requested",
  Cancelled: "cancelled",
  Preparing: "status_preparing",
  Shipped: "shipped",
  Refunded: "status_refunded",
  Processing: "processing",
};

export function translateOrderStatus(status, t) {
  const key = ORDER_STATUS_KEYS[status];
  return key ? t(key) : status;
}

export function translateRole(role, t) {
  const map = { customer: "role_customer", vendor: "role_vendor", admin: "role_admin" };
  return map[role] ? t(map[role]) : role;
}
